int	ft_isalnum(char symb)
{
	if (('a' <= symb && symb <= 'z') || ('A' <= symb && symb <= 'Z'))
	{
		return (1);
	}
	else if ('0' <= symb && symb <= '9')
	{
		return (1);
	}
	return (0);
}
